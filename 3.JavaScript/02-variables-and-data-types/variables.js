//1.var key word
var z; //variable declaration and initialization
z = 50; //variable initialization
z = 60; //variable re-assignment
var z = 70; //re-declaration is allowed with var
console.log(z);


//2.let key word

let x; //variable declaration
x = 10; //variable initialization
x = 20; //variable re-assignment
// let x = 25; //re-declaration is not allowed with let

console.log(x);


//3.const key word
const y = 30; //variable declaration and initialization
console.log(y);
// y = 40; //uncommenting this line will cause an error because const cannot be reassigned
console.log(y)

