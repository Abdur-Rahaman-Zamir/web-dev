let x = 10;   // Assign
console.log(x); // 10

x += 5; // x = x + 5
console.log(x); // 15

x -= 3; // x = x - 3
console.log(x); // 12

x *= 2; // x = x * 2
console.log(x); // 24

x /= 4; // x = x / 4
console.log(x); // 6

x %= 4; // x = x % 4
console.log(x); // 2

x **= 3; // x = x ** 3 (2 এর 3 ঘাত)
console.log(x); // 8
