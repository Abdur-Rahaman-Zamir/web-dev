let x = 10;
let y = 5;

let z = x + y; // Addition
console.log(`Addition: ${z}`); // Output: 15

z = x - y; // Subtraction
console.log(`Subtraction: ${z}`); // Output: 5

z = x * y; // Multiplication
console.log(`Multiplication: ${z}`); // Output: 50

z = x / y; // Division
console.log(`Division: ${z}`); // Output: 2

z = x % y; // Modulus
console.log(`Modulus: ${z}`); // Output: 0

z = x ** y; // Exponentiation
console.log(`Exponentiation: ${z}`); // Output: 100000
