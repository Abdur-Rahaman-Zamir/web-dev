let fruit = "apple";
switch (fruit) {
    case "apple":
        console.log("apple is a red fruit");
    case "banana":
        console.log("banana is yellow fruit");
    case ("pamegranate"):
        console.log("pomegranate is a red fruit");
        break;
    default:
        console.log("unknown fruit");
}

